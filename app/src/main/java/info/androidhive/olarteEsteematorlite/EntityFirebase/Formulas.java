public class Formulas {
        String costoTotal;
        String margen;
        String margenDeseado;
        String nombre;
        String type_formula;
        String ventas;
        public String getCostoTotal() {
            return costoTotal;
        }
        public void setCostoTotal(String costoTotal) {
            this.costoTotal = costoTotal;
        }
        public String getMargen() {
            return margen;
        }
        public void setMargen(String margen) {
            this.margen = margen;
        }
        public String getMargenDeseado() {
            return margenDeseado;
        }
        public void setMargenDeseado(String margenDeseado) {
            this.margenDeseado = margenDeseado;
        }
        public String getNombre() {
            return nombre;
        }
        public void setNombre(String nombre) {
            this.nombre = nombre;
        }
        public String getType_formula() {
            return type_formula;
        }
        public void setType_formula(String type_formula) {
            this.type_formula = type_formula;
        }
        public String getVentas() {
            return ventas;
        }
        public void setVentas(String ventas) {
            this.ventas = ventas;
        }
}
